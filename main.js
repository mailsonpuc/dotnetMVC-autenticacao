
//num 1
const  n1_btn=document.getElementById("n1_btn");
n1_btn.addEventListener("click", function(e){
    e.preventDefault();

    const input=document.getElementById("n1");
    const value = input.value;
    

    if(value == 5){
        alert("acertou é: 5");

        document.getElementById("n1").value = "";
    }
    else{
        alert("Errou")
    }

})






